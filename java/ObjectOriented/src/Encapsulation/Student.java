package Encapsulation;

public class Student {
	private int id;
	private String name;
	private int age;
	
	
	public int getId() {
		return id;
	}
	public void setID(int id) {
		this.id=id;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name=name;
	}
	
	public int getage() {
		return age;
	}
	public void setAge(int age) {
		this.age=age;
	}
}
